// Bitwise operators//

let x=5;
let y=9;
let z=2;

console.log("Bitwise operators "+ (x&y));//1
console.log("Bitwise operators "+ (x|y));//13
console.log("Bitwise operators "+ (x^y));//12
console.log("Bitwise operators "+ (~x));//-6
// left shift
console.log("Bitwise operators "+ (x<<z));//20
// right shift
console.log("Bitwise operators "+ (x>>z));//1

 

